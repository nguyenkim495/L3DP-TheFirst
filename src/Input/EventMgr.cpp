//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool EventMgr::OnEvent(const irr::SEvent& event)
{
	switch (event.EventType)
	{
	case irr::EET_KEY_INPUT_EVENT:
		if(event.KeyInput.Key == 65)
			GetGame()->OnKeyEvent(event.KeyInput);
		
		return true;
		break;
	default:
		break;
	}
	return false;
}
